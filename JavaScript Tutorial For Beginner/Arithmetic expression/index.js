let students = 20;
students = students + 1; // students += 1;
console.log(students);

students = students - 1; // students -= 1;
console.log(students);

students = students * 2; // students *= 2;
console.log(students);

students = students / 10; // students /= 10;
console.log(students);

let extraStudents = students % 3;
console.log(extraStudents);